#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char		*p_s1;
	const char	*p_s2 = s2;

	p_s1 = s1;
	while (*p_s1)
		p_s1++;
	while ((*p_s1++ = *p_s2++))
		;
	*p_s1 = '\0';
	return (s1);
}
